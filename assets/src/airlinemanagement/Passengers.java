    private void SaveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveBtnMouseClicked
        if(PNameTb.getText().isEmpty() || PassNumTb.getText().isEmpty() || PPhoneTb.getText().isEmpty() || PAddressTb.getText().isEmpty() || NatCb.getSelectedIndex()== -1 || GenCb.getSelectedIndex()== -1 ){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else{
            try {
                Con = DatabaseConfig.getConnection();
                PreparedStatement Add = Con.prepareStatement("insert into PassengerTbl values(?,?,?,?,?,?,?)");
                Add.setInt(1, PassId);
                Add.setString(2, PNameTb.getText());
                Add.setString(3, NatCb.getSelectedItem().toString());
                Add.setString(4, GenCb.getSelectedItem().toString());
                Add.setString(5, PassNumTb.getText());
                Add.setString(6, PPhoneTb.getText());
                Add.setString(7, PAddressTb.getText());
                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Passenger Added");
                Con.close();
                DisplayPassengers();
                Clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error adding passenger: " + e.getMessage());
            } finally {
                DatabaseConfig.closeConnection(Con);
            }
        }
    }//GEN-LAST:event_SaveBtnMouseClicked

    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(Key==0){
            JOptionPane.showMessageDialog(this, "Please Select a Passenger");
        }else{
            try{
                Con = DatabaseConfig.getConnection();
                String Query = "Delete from PassengerTbl where PassId="+Key;
                Statement Del = Con.createStatement();
                Del.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Passenger Deleted");
                DisplayPassengers();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error deleting passenger: " + e.getMessage());
            } finally {
                DatabaseConfig.closeConnection(Con);
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

