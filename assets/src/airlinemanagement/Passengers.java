    private void EditBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditBtnMouseClicked
        if(Key==0){
            JOptionPane.showMessageDialog(this, "Select a Passenger");
        }else{
            try {
                Con = DatabaseConfig.getConnection();
                String Query = "Update PassengerTbl set PassName=?, PassNationality=?, PassGender=?, PassPassport=?, PassPhone=?, PassAddress=? where PassId=?";
                PreparedStatement Add = Con.prepareStatement(Query);
                Add.setString(1, PNameTb.getText());
                Add.setString(2, NatCb.getSelectedItem().toString());
                Add.setString(3, GenCb.getSelectedItem().toString());
                Add.setString(4, PassNumTb.getText());
                Add.setString(5, PPhoneTb.getText());
                Add.setString(6, PAddressTb.getText());
                Add.setInt(7, Key);
                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Passenger Updated");
                Con.close();
                DisplayPassengers();
                Clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error updating passenger: " + e.getMessage());
            } finally {
                DatabaseConfig.closeConnection(Con);
            }
        }
    }//GEN-LAST:event_EditBtnMouseClicked

