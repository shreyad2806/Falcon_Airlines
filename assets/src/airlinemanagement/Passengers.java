    private void DisplayPassengers(){
        try{
            Con = DatabaseConfig.getConnection();
            St = Con.createStatement();
            Rs = St.executeQuery("select * from PassengerTbl");
            
            // Create table model manually since DbUtils is not available
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Passenger ID");
            model.addColumn("Name");
            model.addColumn("Nationality");
            model.addColumn("Gender");
            model.addColumn("Passport");
            model.addColumn("Phone");
            model.addColumn("Address");
            
            while(Rs.next()) {
                model.addRow(new Object[]{
                    Rs.getString("PassId"),
                    Rs.getString("PassName"),
                    Rs.getString("PassNationality"),
                    Rs.getString("PassGender"),
                    Rs.getString("PassPassport"),
                    Rs.getString("PassPhone"),
                    Rs.getString("PassAddress")
                });
            }
            
            PassengersTable.setModel(model);
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error displaying passengers: " + e.getMessage());
        } finally {
            DatabaseConfig.closeConnection(Con);
        }
    }
    
    int PassId = 0;
    private void CountPassengers()
    {
        try{
            Con = DatabaseConfig.getConnection();
            St = Con.createStatement();
            Rs = St.executeQuery("select max(PassId) from PassengerTbl");
            while(Rs.next()){
                PassId = Rs.getInt(1);
            }
            PassId = PassId + 1;
        }catch(Exception e){
            PassId = 1;
        } finally {
            DatabaseConfig.closeConnection(Con);
        }
    }
    
